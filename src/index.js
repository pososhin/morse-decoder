const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
    '*':  ' ',
};
const REXP000 = /^0*/;
const REXPDOT = /10/g;
const REXPDASH =/11/g;
const REXPSPACE = /\*+/g;
function decode(expr) {
    if (!expr || expr.length <10) return '';
    return MORSE_TABLE[ expr.substr(0,10)
        .replace(REXP000,'')
        .replace(REXPDOT,'.')
        .replace(REXPDASH,'-')
        .replace(REXPSPACE,'*')
    ]+decode(expr.substr(10));
}

module.exports = {
    decode
}

